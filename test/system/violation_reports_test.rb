require "application_system_test_case"

class ViolationReportsTest < ApplicationSystemTestCase
  setup do
    @violation_report = violation_reports(:one)
  end

  test "visiting the index" do
    visit violation_reports_url
    assert_selector "h1", text: "Violation reports"
  end

  test "should create violation report" do
    visit violation_reports_url
    click_on "New violation report"

    fill_in "Latitude", with: @violation_report.latitude
    fill_in "Longitude", with: @violation_report.longitude
    fill_in "Vehicle count", with: @violation_report.vehicle_count
    click_on "Create Violation report"

    assert_text "Violation report was successfully created"
    click_on "Back"
  end

  test "should update Violation report" do
    visit violation_report_url(@violation_report)
    click_on "Edit this violation report", match: :first

    fill_in "Latitude", with: @violation_report.latitude
    fill_in "Longitude", with: @violation_report.longitude
    fill_in "Vehicle count", with: @violation_report.vehicle_count
    click_on "Update Violation report"

    assert_text "Violation report was successfully updated"
    click_on "Back"
  end

  test "should destroy Violation report" do
    visit violation_report_url(@violation_report)
    accept_confirm { click_on "Destroy this violation report", match: :first }

    assert_text "Violation report was successfully destroyed"
  end
end
